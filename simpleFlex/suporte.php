            <p>A importância da manutenção preventiva</p>
            <p>É compreensível que, em seu dia a dia, a preocupação com a área de TI não seja uma urgência. Afinal, o que deve ser priorizado são as atividades ligadas diretamente a sua especialidade. Mas, para evitar que o seu computador pare inesperadamente e prejudique toda a sua rotina, programe um check-up geral em sua máquina.</p>
