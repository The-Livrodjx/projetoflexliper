            <p><a>Antivírus</a></p>
            <p>O suporte técnico em TI será valioso na escolha da opção de antivírus adequada para o negócio. Apesar de existirem inúmeras alternativas gratuitas no mercado, o mais recomendado é fazer uso de uma versão completa. O que implica o custo de assinatura, porém agrega recursos extras para a proteção da rede.</p>
            <p>O trabalho de escolher o antivírus ideal para as necessidades do negócio será do profissional de TI. Ele também poderá explorar todas as soluções da ferramenta, garantindo total proveito do investimento.</p>
